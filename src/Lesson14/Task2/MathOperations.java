package Lesson14.Task2;

public class MathOperations {

    public int operation(int a, int b){
        return a+b;
    }

    public int operation(int a, int b,int c){
        return (a-b-c);
    }

    public double operation(int a, double b){
        return a*b;
    }

    public double operation(double a, int b){
        return a/b;
    }


}
